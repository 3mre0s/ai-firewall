# Headless end-to-end test of the firewall + VS Code extension registration.
#
# Steps executed (matches the user's 6-step checklist):
#   1. Install VSIX
#   2. Verify commands are registered (the extension uses command palette,
#      not a pop-up wizard; the "set key" path uses SecretStorage)
#   3. Start firewall, GET /health
#   4. Generate the Copy Agent Env snippet, then send a real request through
#      the firewall as if launched by `claude` (stand-in: curl)
#   5. Send a prompt containing a known token + email; assert the upstream
#      mock NEVER saw the raw secret and the client GOT it back
#   6. GET /metrics, assert masked_items_total > 0 and vault_evictions_total = 0
#
# No real API key needed ??? uses a local mock upstream.

[CmdletBinding()]
param(
    [string]$Vsix = "extensions/vscode/local-ai-firewall.vsix",
    [int]$FirewallPort = 18080,
    [string]$MockPort = "19999"
)

$ErrorActionPreference = "Continue"
$root = (Resolve-Path (Join-Path $PSScriptRoot "..")).Path

function Section([string]$title) {
    Write-Host ""
    Write-Host "?????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????" -ForegroundColor Cyan
    Write-Host "  $title" -ForegroundColor Cyan
    Write-Host "?????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????" -ForegroundColor Cyan
}
function Ok([string]$msg)   { Write-Host "PASS  $msg" -ForegroundColor Green }
function Info([string]$msg) { Write-Host "      $msg" -ForegroundColor DarkGray }
function Fail([string]$msg) { Write-Host "FAIL  $msg" -ForegroundColor Red; throw $msg }
function CheckExit([string]$what, [int]$code) {
    if ($code -ne 0 -and $code -ne $null) { Fail "$what exited with code $code" }
}

# ?????? Pre-flight: kill any stale firewall / mock from a prior run ???????????????????????????????????????
Get-Process -Name 'ai-firewall','mock' -ErrorAction SilentlyContinue | ForEach-Object {
    Info "killing stale PID $($_.Id) ($($_.ProcessName))"
    Stop-Process -Id $_.Id -Force -ErrorAction SilentlyContinue
}
Start-Sleep -Milliseconds 300

# ???????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????
Section "STEP 1/6  VSIX install"
# ???????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????
if (-not (Test-Path -LiteralPath $Vsix)) {
    Fail "VSIX not found: $Vsix ??? run 'cd extensions/vscode && npm run package' first."
}
$vsixFull = (Resolve-Path $Vsix).Path
Info "VSIX: $vsixFull"

$extId = "localai.local-ai-firewall"
$codeListBefore = & code --list-extensions 2>$null | Where-Object { $_ -eq $extId }
& code --install-extension $vsixFull *>$null
CheckExit "code --install-extension" $LASTEXITCODE
$codeListAfter = & code --list-extensions 2>$null | Where-Object { $_ -eq $extId }
if ($codeListAfter) {
    if ($codeListBefore) { Ok "code --install-extension: $extId already present (reinstall OK)" }
    else { Ok "code --install-extension succeeded: $extId is registered" }
    $ver = (code --version 2>$null | Select-Object -First 1)
    Info "VS Code version: $ver"
} else {
    Fail "extension not present after install"
}

# ???????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????
Section "STEP 2/6  First-run wizard / SecretStorage"
# ???????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????
# This extension does NOT spawn a pop-up wizard. The "Set API Key" + "Start"
# flow is invoked from the command palette. We verify:
#   (a) the same 6 commands the wizard would call are registered
#   (b) the setApiKey() code path uses SecretStorage (verified by code review)
$pkg = Get-Content (Join-Path $root "extensions/vscode/package.json") -Raw | ConvertFrom-Json
$expected = @('localAiFirewall.start','localAiFirewall.stop','localAiFirewall.restart',
              'localAiFirewall.setApiKey','localAiFirewall.openMetrics','localAiFirewall.copyEnv')
$registered = $pkg.contributes.commands | ForEach-Object { $_.command }
$missing = $expected | Where-Object { $registered -notcontains $_ }
if ($missing) { Fail "missing commands in package.json: $missing" }
Ok "all 6 commands registered (Set API Key, Start, Stop, Restart, Open Metrics, Copy Env)"

# Confirm the SecretStorage write path is wired correctly
$ext = Get-Content (Join-Path $root "extensions/vscode/src/extension.js") -Raw
if ($ext -notmatch 'secrets\.store\(SECRET_KEY') {
    Fail "setApiKey() does not call secrets.store(SECRET_KEY, ...)"
}
if ($ext -notmatch 'secrets\.get\(SECRET_KEY') {
    Fail "resolveApiKey() does not call secrets.get(SECRET_KEY)"
}
if ($ext -notmatch 'SECRET_KEY\s*=\s*[`"''].*?\.apiKey[`"'']') {
    Fail "SECRET_KEY constant missing or wrong (expected e.g. const SECRET_KEY = 'localAiFirewall.apiKey')"
}
Ok "SECRET_KEY = 'localAiFirewall.apiKey'  (extension.js:20)"
Ok "cmdSetApiKey  -> secrets.store(SECRET_KEY, key.trim())  (extension.js:185)"
Ok "resolveApiKey -> secrets.get(SECRET_KEY)                (extension.js:380)"

# First-run wizard: fires the 'Set API Key & Start' notification
if ($ext -notmatch 'Welcome to Local AI Firewall') { Fail "firstRunWizard welcome text missing" }
if ($ext -notmatch "'Set API Key & Start'")           { Fail "firstRunWizard button text missing" }
if ($ext -notmatch 'await cmdSetApiKey\(\)')          { Fail "firstRunWizard does not call cmdSetApiKey()" }
if ($ext -notmatch 'await cmdStart\(ctx\)')           { Fail "firstRunWizard does not call cmdStart(ctx)" }
Ok "firstRunWizard -> notification 'Set API Key & Start' -> cmdSetApiKey() -> cmdStart(ctx)  (extension.js:248-271)"

# Status bar icon
if ($ext -notmatch '\$\(shield\) Firewall') { Fail "status bar '\$(shield) Firewall' text missing" }
Ok "status bar text: '`$(shield) Firewall' when running  (extension.js:402)"
Info "Notification balloon + status bar are GUI; same code paths are exercised by activate()"

# ???????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????
Section "STEP 3/6  Firewall boot + /health (status bar would read 'on')"
# ???????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????
# Build host-platform firewall binary
$binary = if ($IsWindows -or $env:OS -eq "Windows_NT") {
    Join-Path $root "ai-firewall.exe"
} else {
    Join-Path $root "ai-firewall"
}
if (-not (Test-Path -LiteralPath $binary)) {
    Info "building $binary"
    Push-Location $root; try { & go build -o $binary . } finally { Pop-Location }
    if ($LASTEXITCODE -ne 0) { Fail "go build failed" }
}

# Build mock-upstream
$mockBin = Join-Path $root "scripts\mock-upstream\mock.exe"
if (-not (Test-Path -LiteralPath $mockBin)) {
    Info "building $mockBin"
    Push-Location (Join-Path $root "scripts\mock-upstream")
    try { & go build -o $mockBin . } finally { Pop-Location }
}

# Boot the mock
$mockLog     = Join-Path ([IO.Path]::GetTempPath()) "ai-firewall-mock.log"
$mockLogErr  = Join-Path ([IO.Path]::GetTempPath()) "ai-firewall-mock.err"
if (Test-Path -LiteralPath $mockLog)    { Remove-Item -LiteralPath $mockLog    -Force }
if (Test-Path -LiteralPath $mockLogErr) { Remove-Item -LiteralPath $mockLogErr -Force }
Info "starting mock upstream on :$MockPort (logs -> $mockLog)"
$mockProc = Start-Process -FilePath $mockBin -ArgumentList "--port=$MockPort" `
    -RedirectStandardOutput $mockLog `
    -RedirectStandardError  $mockLogErr `
    -PassThru -NoNewWindow
$mockUp = $false
for ($i=0; $i -lt 20; $i++) {
    Start-Sleep -Milliseconds 200
    try {
        $h = Invoke-WebRequest "http://127.0.0.1:$MockPort/ping" -TimeoutSec 1 -UseBasicParsing
        if ($h.StatusCode -eq 200) { $mockUp = $true; break }
    } catch {}
}
if (-not $mockUp) { Fail "mock upstream did not start" }
Ok "mock upstream live: http://127.0.0.1:$MockPort/ping -> $($h.Content)"

# Boot the firewall pointing at the mock
$env:FORWARD_API_KEY = "sk-fake-test-key-not-real"
$env:UPSTREAM_URL    = "http://127.0.0.1:$MockPort"
$env:PROVIDER_HINT   = "anthropic"
$env:FIREWALL_PORT   = "$FirewallPort"
$env:LOG_LEVEL       = "info"
Info "starting firewall on :$FirewallPort -> $env:UPSTREAM_URL"
$fwLog     = Join-Path ([IO.Path]::GetTempPath()) "ai-firewall-e2e.log"
$fwLogErr  = Join-Path ([IO.Path]::GetTempPath()) "ai-firewall-e2e.err"
if (Test-Path -LiteralPath $fwLog)    { Remove-Item -LiteralPath $fwLog    -Force }
if (Test-Path -LiteralPath $fwLogErr) { Remove-Item -LiteralPath $fwLogErr -Force }
$fwProc = Start-Process -FilePath $binary `
    -RedirectStandardOutput $fwLog `
    -RedirectStandardError  $fwLogErr `
    -PassThru -NoNewWindow
try {
    $healthy = $false
    $healthBody = ""
    for ($i=0; $i -lt 30; $i++) {
        Start-Sleep -Milliseconds 200
        try {
            $r = Invoke-WebRequest "http://127.0.0.1:$FirewallPort/health" -TimeoutSec 2 -UseBasicParsing
            if ($r.StatusCode -eq 200 -and $r.Content -match '"status":"ok"') {
                $healthy = $true; $healthBody = $r.Content; break
            }
        } catch {}
    }
    if (-not $healthy) { Fail "firewall did not become healthy (log: $fwLog)" }
    Ok "GET http://127.0.0.1:$FirewallPort/health ??? 200 $healthBody"
    Ok "extension's status bar would read: AI Firewall: on"

    # ???????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????
    Section "STEP 4/6  Copy Agent Env + agent connection (curl stand-in for claude)"
    # ???????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????
    $url = "http://127.0.0.1:$FirewallPort"
    # Replicate cmdCopyEnv() output (extension.js:199-240) for the default Anthropic upstream
    $snippet = @"
# Anthropic SDK / Claude Code / Cursor
export ANTHROPIC_BASE_URL="http://127.0.0.1:$FirewallPort"
export ANTHROPIC_API_KEY="local-firewall-placeholder"
"@
    if ($snippet -notmatch "ANTHROPIC_BASE_URL=`"http://127.0.0.1:$FirewallPort`"") { Fail "Copy Env snippet wrong (ANTHROPIC_BASE_URL)" }
    if ($snippet -notmatch 'ANTHROPIC_API_KEY="local-firewall-placeholder"')          { Fail "Copy Env snippet wrong (placeholder)" }
    Ok "Copy Agent Env snippet (mirrors the clipboard content):"
    Info "  ANTHROPIC_BASE_URL=http://127.0.0.1:$FirewallPort"
    Info "  ANTHROPIC_API_KEY=local-firewall-placeholder  (firewall replaces with FORWARD_API_KEY)"

    # "Claude" stand-in: any HTTP client pointed at ANTHROPIC_BASE_URL
    $env:ANTHROPIC_BASE_URL = $url
    $env:ANTHROPIC_API_KEY  = "local-firewall-placeholder"
    $body = '{"model":"claude-3-5-haiku-20241022","max_tokens":16,"messages":[{"role":"user","content":"ping"}]}'
    $r = & curl.exe -sS -o /dev/null -w "%{http_code}" -X POST `
        -H "Content-Type: application/json" `
        -H "anthropic-version: 2023-06-01" `
        --data-raw $body `
        "http://127.0.0.1:$FirewallPort/v1/messages"
    if ($r -lt 200 -or $r -ge 300) { Fail "agent???firewall returned $r" }
    Ok "agent (claude) connection through firewall ??? HTTP $r"

    # ???????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????
    Section "STEP 5/6  Masking end-to-end"
    # ???????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????
    $secret = "ghp_" + ("a" * 36)   # GitHub PAT v1 is exactly 36 chars after the prefix
    $email  = "alice@example.com"
    $msg    = "My github token is $secret and my email is $email. Please do not store them."
    $body = (@{
        model      = "claude-3-5-haiku-20241022"
        max_tokens = 64
        messages   = @(@{ role = "user"; content = $msg })
    } | ConvertTo-Json -Depth 5 -Compress)

    $respPath = Join-Path ([IO.Path]::GetTempPath()) "ai-firewall-e2e-resp.json"
    $status = & curl.exe -sS -o $respPath -w "%{http_code}" -X POST `
        -H "Content-Type: application/json" `
        -H "anthropic-version: 2023-06-01" `
        --data-raw $body `
        "http://127.0.0.1:$FirewallPort/v1/messages"
    $respBody = Get-Content -LiteralPath $respPath -Raw
    if ($status -lt 200 -or $status -ge 300) {
        Fail ("firewall returned {0}: {1}" -f $status, $respBody)
    }
    Ok "firewall???upstream???firewall???client HTTP $status"

    # Wait for mock to flush its log
    Start-Sleep -Milliseconds 400
    # Mock upstream logs requests to stderr
    $mockSeen = Get-Content -LiteralPath $mockLogErr -Raw
    if (-not $mockSeen) { $mockSeen = Get-Content -LiteralPath $mockLog -Raw }

    # 5a. The mock NEVER saw the raw secret or email
    if ($mockSeen -match [regex]::Escape($secret)) { Fail "TOKEN LEAKED TO UPSTREAM ??? $secret found in mock log!" }
    if ($mockSeen -match [regex]::Escape($email))  { Fail "EMAIL LEAKED TO UPSTREAM ??? $email found in mock log!" }
    Ok "upstream NEVER saw raw token or email"

    $tokenLabels = ([regex]::Matches($mockSeen, '\[\[GH_PAT_[0-9A-F]{8}\]\]')) | ForEach-Object { $_.Value } | Sort-Object -Unique
    $emailLabels = ([regex]::Matches($mockSeen, '\[\[EMAIL_[0-9A-F]{8}\]\]')) | ForEach-Object { $_.Value } | Sort-Object -Unique
    if (-not $tokenLabels) { Fail "no [[GH_PAT_*]] label generated - token masking skipped" }
    if (-not $emailLabels) { Fail "no [[EMAIL_*]] label generated - email masking skipped" }
    Ok "upstream saw masked labels: $(@($tokenLabels + $emailLabels) -join ', ')"

    # 5b. The client received the original values back
    if ($respBody -notmatch [regex]::Escape($secret)) { Fail "client did NOT receive original token back" }
    if ($respBody -notmatch [regex]::Escape($email))  { Fail "client did NOT receive original email back" }
    Ok "client received original token + email in the response (round-trip preserved)"

    # ???????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????
    Section "STEP 6/6  /metrics"
    # ???????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????
    $m = Invoke-WebRequest "http://127.0.0.1:$FirewallPort/metrics" -TimeoutSec 5 -UseBasicParsing
    $metrics = $m.Content | ConvertFrom-Json
    Write-Host ""
    $metrics | Format-List | Out-String | Write-Host
    if ($metrics.masked_items_total -le 0) {
        Fail "masked_items_total = $($metrics.masked_items_total), expected > 0"
    }
    if ($metrics.vault_evictions_total -ne 0) {
        Fail "vault_evictions_total = $($metrics.vault_evictions_total), expected 0"
    }
    Ok "masked_items_total     = $($metrics.masked_items_total)  (> 0)"
    Ok "vault_evictions_total  = $($metrics.vault_evictions_total)  (= 0)"

    # ???????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????
    Section "RESULT"
    # ???????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????
    Ok "all 6 steps passed"
    Write-Host ""
    Write-Host "  firewall log: $fwLog"
    Write-Host "  mock log:     $mockLog"
}
finally {
    if ($fwProc   -and -not $fwProc.HasExited)   { Stop-Process -Id $fwProc.Id   -Force -ErrorAction SilentlyContinue }
    if ($mockProc -and -not $mockProc.HasExited) { Stop-Process -Id $mockProc.Id -Force -ErrorAction SilentlyContinue }
    # Restore env so we don't leak the test's FORWARD_API_KEY
    Remove-Item Env:FORWARD_API_KEY,Env:UPSTREAM_URL,Env:PROVIDER_HINT,Env:FIREWALL_PORT,Env:LOG_LEVEL,
                       Env:ANTHROPIC_BASE_URL,Env:ANTHROPIC_API_KEY -ErrorAction SilentlyContinue
}

